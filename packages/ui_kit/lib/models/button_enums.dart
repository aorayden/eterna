/// Виды (размеры) кнопки.
///
/// Определяет габариты виджета: высоту, внутренние отступы (padding) и размер текста.
enum AppButtonKind {
  /// Большой размер.
  /// Стандартный размер для основных действий (CTA) и форм.
  /// Обычно используется как кнопка на всю ширину экрана или в главных блоках.
  large,

  /// Маленький размер.
  /// Используется во вспомогательных блоках, модальных окнах или там, где пространство ограничено.
  small,

  /// Компактный вид "чипа" (таблетки).
  /// Обычно имеет сильное скругление углов и минимальную высоту.
  /// Подходит для фильтров, тегов или контекстных действий в списках.
  chip,
}

/// Стили (варианты внешнего вида) кнопки.
///
/// Определяет визуальное оформление: цвет фона, цвет текста и наличие границ.
/// Позволяет управлять визуальной иерархией (приоритетом) действий на экране.
enum AppButtonVariant {
  /// Залитая кнопка (Filled).
  /// Имеет цветной фон (обычно [AppColors.accent]).
  /// Используется для самого важного действия на экране (Primary action).
  filled,

  /// Контурная кнопка (Outlined).
  /// Прозрачный фон с цветной обводкой.
  /// Используется для второстепенных действий (Secondary action), которые не должны перетягивать внимание с основной кнопки.
  outlined,

  /// "Призрачная" кнопка (Ghost / Text Button).
  /// Не имеет ни фона, ни видимой границы (до нажатия).
  /// Выглядит как кликабельный текст. Используется для наименее приоритетных действий (Tertiary action), например "Отмена".
  ghost,
}
